                        <h3>Our History.</h3>

                        <p>“We should buy a dozer and rent it to a local demolition company.” This casual remark between
                            two young friends – Ben Kates Jr and Mike Hamilton – led to what is now Oklahoma’s largest
                            demolition company. Ben was an equipment operator for a local demolition company and Mike
                            operated a distributorship in bulk fuel. With $500 each and a $1000 loan from the local
                            bank, Ben and Mike purchased a dozer and operated under the name Kates-Hamilton Dozer
                            Service.</p>

                        <p>
                            The dozer service might have stayed in operation but Ben’s employer transferred him to
                            Colorado. During that time an opportunity arose for Ben and Mike to perform a demolition
                            project. Ben called his partner back in Oklahoma and asked him if he was interested in
                            bidding this small job in Crook, Colorado. The year was 1976 and Midwest Wrecking Co had its
                            first job. With another successful bid on a project in Marietta, Oklahoma, Ben moved his
                            family back to Oklahoma. At first, Midwest Wrecking Co had a hard time convincing clients
                            they were capable of handling a large project. The break they were waiting for came in 1982.
                            After lengthy negotiations Midwest Wrecking Co was finally awarded a contract to demolish
                            the Oklahoma City Baptist Children’s Home to make way for a new business complex. The
                            contract required complete demolition of 20 buildings within 30 days. Midwest Wrecking Co
                            completed the project in 18 days. The high profile project received a lot of favorable
                            publicity in the newspaper and praise from the General Contractor. Midwest Wrecking Co had
                            proven they could complete a large project – quickly, safety, and efficiently.
                        </p>

                        <p>
                            Today Midwest Wrecking Co is the largest demolition company in the state of Oklahoma.
                            Midwest Wrecking Co. has successfully completed every project on which they have worked.
                            Clients know they can count on Midwest Wrecking Co to complete each project with the utmost
                            diligence.
                        </p>
